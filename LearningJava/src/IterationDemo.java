
public class IterationDemo {

	public static void main(String[] args) {
		// While, Do while, For, For each
		
		int i=5;
		int j=0;
		int k=0;
		//While Loop
		
		while(j<i) {
			
			System.out.println(j);
			j++;
			}
      // DO while loop
		do {
			System.out.println(k);
			k++;
		}while(k<i);
		
	// For loop
	
	for(int l=0;l<i;l++) {
		System.out.println(l);
	}

	}
}
